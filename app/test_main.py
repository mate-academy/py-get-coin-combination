import pytest
from app.main import get_coin_combination  # Проверь правильность пути


@pytest.mark.parametrize(
    "cents, expected",
    [
        (0, [0, 0, 0, 0]),  # Нет монет
        (1, [1, 0, 0, 0]),  # Только пенни
        (4, [4, 0, 0, 0]),  # Несколько пенни
        (5, [0, 1, 0, 0]),  # Только никель
        (10, [0, 0, 1, 0]),  # Только дайм
        (25, [0, 0, 0, 1]),  # Только четвертак
        (6, [1, 1, 0, 0]),  # Пенни + никель
        (11, [1, 0, 1, 0]),  # Пенни + дайм
        (15, [0, 1, 1, 0]),  # Никель + дайм
        (17, [2, 1, 1, 0]),  # Пенни + никель + дайм
        (30, [0, 1, 0, 1]),  # Только даймы
        (41, [1, 1, 1, 1]),  # Все виды монет
        (50, [0, 0, 0, 2]),  # Только четвертаки
        (99, [4, 0, 2, 3]),  # Комбинация всех монет
        (100, [0, 0, 0, 4]),  # 4 четвертака
        (123, [3, 0, 2, 4]),  # Смешанная комбинация
    ]
)
def test_get_coin_combination(cents: int, expected: list[int]) -> None:
    assert get_coin_combination(cents) == expected


# Тест для проверки возвращения только одного типа монет
def test_return_only_one_type() -> None:  # Добавлены аннотации типов
    """Проверяет, что функция может возвращать только один тип монет"""
    assert get_coin_combination(5) == [0, 1, 0, 0]  # Только никель
    assert get_coin_combination(10) == [0, 0, 1, 0]  # Только дайм
    assert get_coin_combination(25) == [0, 0, 0, 1]  # Только четвертак
    assert get_coin_combination(50) == [0, 0, 0, 2]  # Только четвертаки


# Тест для проверки смешанных типов монет
def test_return_mixed_coins() -> None:  # Добавлены аннотации типов
    """Проверяет, что функция корректно возвращает комбинацию разных монет"""
    assert get_coin_combination(6) == [1, 1, 0, 0]  # Пенни + никель
    assert get_coin_combination(11) == [1, 0, 1, 0]  # Пенни + дайм
    assert get_coin_combination(17) == [2, 1, 1, 0]  # Пенни + никель + дайм
    assert get_coin_combination(41) == [1, 1, 1, 1]  # Все виды монет
